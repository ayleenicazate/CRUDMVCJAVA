    private void jbtn_verCarritoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtn_verCarritoActionPerformed
        DefaultTableModel modelo = (DefaultTableModel) this.jtbl_comprarCarrito.getModel();
        modelo.setRowCount(0); // Limpiamos el modelo de la tabla

        RegistroCarrito rc = new RegistroCarrito();
        List<CarritoDeCompras> lista = rc.buscarTodos();

        for (CarritoDeCompras carrito : lista) {
            String nombreProd = carrito.getNombre_prod();
            int cantidad = carrito.getCantidad();
            int precio = carrito.getPrecio();

            modelo.addRow(new Object[]{nombreProd, cantidad, precio});
        }
    }//GEN-LAST:event_jbtn_verCarritoActionPerformed

