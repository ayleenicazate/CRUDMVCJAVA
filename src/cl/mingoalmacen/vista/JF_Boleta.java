    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // Mostrar un JOptionPane de agradecimiento por la compra
        JOptionPane.showMessageDialog(null, "¡Gracias por su compra y por siempre preferirnos!"+"\n"+"No se pierda nuestras ofertas de la próxima semana."+"\n"+"\t\t¡Lo esperamos!", "Compra Exitosa", JOptionPane.INFORMATION_MESSAGE);

        // Llamar al método para borrar los elementos del carrito
        RegistroCarrito rc = new RegistroCarrito();
        boolean elementosBorrados = rc.borrarTodo();

        if (elementosBorrados) {
            JOptionPane.showMessageDialog(null, "Ha comprado todo los producto del carrito. Por favor, vuelva atrás si desea seguir comprando.");
        } else {
            JOptionPane.showMessageDialog(null, "No se han encontrado elementos en el carrito para borrar.");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

